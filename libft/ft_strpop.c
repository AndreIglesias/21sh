#include "libft.h"

char	*ft_strpop(char *str, size_t i)
{
	if (str && i <= ft_strlen(str))
	{
		str[i] = 0;
		if (str[i + 1])
			str = ft_fstrjoin(str, &str[i + 1]);
	}
	return (str);
}
