#include "libft.h"

double	ft_atod(char *str)
{
	double	a;
	double	n;
	int		i;
	int		e;

	e = 1;
	i = 0;
	n = 1.;
	a = 0.;
	if (str[i] == '-')
	{
		i++;
		n = -1.;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		a = (a * 10.) + (n * (str[i++] - 48.));
		if (str[i] == '.')
		{
			i++;
			while (str[i] && str[i] >= '0' && str[i] <= '9')
				a = a + (n * (str[i++] - 48.) / ft_exp(10, e++));
		}
	}
	return (a);
}
